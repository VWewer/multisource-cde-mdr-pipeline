# ── Type coercion helpers ──────────────────────────────────────────────────────
def coerce_bool(val: str):
    """Convert string boolean representations to Python bool or None."""
    if val in ("True", "true", "1", "yes"):  return True
    if val in ("False", "false", "0", "no"): return False
    return None

def coerce_int(val: str):
    """Convert string to int or None."""
    try:    return int(val)
    except: return None

def coerce_ts(val: str):
    """Return ISO timestamp string or None."""
    return val.strip() if val.strip() else None

def coerce_date(val: str):
    """Return date string or None."""
    return val.strip() if val.strip() else None

# ── Load a CSV into a Snowflake table ────────────────────────────────────────
def load_csv(cursor, csv_path: Path, table: str, bool_fields: set, int_fields: set, ts_fields: set, date_fields: set):
    with open(csv_path, encoding="utf-8") as f:
        rows = list(csv.DictReader(f))

    if not rows:
        print(f"  ⚠ No rows in {csv_path}")
        return

    columns   = list(rows[0].keys())
    col_list  = ", ".join(columns)
    placeholders = ", ".join(["%s"] * len(columns))
    insert_sql = f"INSERT INTO {table} ({col_list}) VALUES ({placeholders})"

    batch = []
    for row in rows:
        values = []
        for col in columns:
            val = row[col]
            if col in bool_fields:   values.append(coerce_bool(val))
            elif col in int_fields:  values.append(coerce_int(val))
            elif col in ts_fields:   values.append(coerce_ts(val))
            elif col in date_fields: values.append(coerce_date(val))
            else:                    values.append(val if val != "" else None)
        batch.append(tuple(values))

    cursor.executemany(insert_sql, batch)
    print(f"  ✓ Loaded {len(batch)} rows → {table}")

# ── Main ───────────────────────────────────────────────────────────────────────
def main():
    # Check CSVs exist
    for p in [RAW_CSV, STAGED_CSV, MDR_CSV]:
        if not p.exists():
            print(f"✗ Missing: {p}")
            print("  Run the three generate_*.py scripts first.")
            return

    # Connect to Snowflake
    print("Connecting to Snowflake...")
    try:
        connect_kwargs = {
            "user":      os.environ["SNOWFLAKE_USER"],
            "password":  os.environ["SNOWFLAKE_PASSWORD"],
            "account":   os.environ["SNOWFLAKE_ACCOUNT"],
            "warehouse": os.environ["SNOWFLAKE_WAREHOUSE"],
            "database":  os.environ["SNOWFLAKE_DATABASE"],
            "schema":    os.environ.get("SNOWFLAKE_SCHEMA"),
        }
        role = os.environ.get("SNOWFLAKE_ROLE")
        if role:
            connect_kwargs["role"] = role

        conn = snowflake.connector.connect(**connect_kwargs)
    except KeyError as e:
        print(f"✗ Missing environment variable: {e}")
        print("  Check your .env file against .env.example")
        return
    except Exception as e:
        print(f"✗ Snowflake connection failed: {e}")
        return

    cursor = conn.cursor()

    # Ensure database and schemas exist
    db = os.environ["SNOWFLAKE_DATABASE"]
    print("\nCreating database if not exists...")
    cursor.execute(f"CREATE DATABASE IF NOT EXISTS {db}")
    cursor.execute(f"USE DATABASE {db}")

    print("\nCreating schemas if not exists...")
    for schema in ["RAW", "STAGED", "ANALYTICAL"]:
        cursor.execute(f"CREATE SCHEMA IF NOT EXISTS {schema}")

    # Create tables
    print("\nCreating tables...")
    for name, ddl in [("RAW.DOCUMENTS", DDL_RAW), ("STAGED.EVENTS", DDL_STAGED), ("ANALYTICAL.MDR_REQUIREMENTS", DDL_MDR)]:
        cursor.execute(ddl)
        print(f"  ✓ Table {name}")

    # Load data
    print("\nLoading data...")

    load_csv(
        cursor, RAW_CSV, "RAW.DOCUMENTS",
        bool_fields  = {"certification_required", "sensitive_information"},
        int_fields   = set(),
        ts_fields    = {"ingested_at"},
        date_fields  = {"issue_date"},
    )

    load_csv(
        cursor, STAGED_CSV, "STAGED.EVENTS",
        bool_fields  = set(),
        int_fields   = {"variance_days", "days_in_previous_status", "review_cycle"},
        ts_fields    = {"planned_timestamp", "actual_timestamp"},
        date_fields  = set(),
    )

    load_csv(
        cursor, MDR_CSV, "ANALYTICAL.MDR_REQUIREMENTS",
        bool_fields  = {"certification_required", "sensitive_information", "is_on_critical_path"},
        int_fields   = {"schedule_float_days", "total_slip_days", "recent_slip_days",
                        "derived_percent_complete", "reported_percent_complete"},
        ts_fields    = {"last_status_change"},
        date_fields  = {"planned_submission_date", "planned_approval_date",
                        "baseline_approval_date", "previous_approval_date"},
    )

    conn.commit()
    cursor.close()
    conn.close()
    print("\n✓ All done. Snowflake load complete.")
    print(f"  Database : {os.environ['SNOWFLAKE_DATABASE']}")
    print(f"  Tables   : RAW.DOCUMENTS, STAGED.EVENTS, ANALYTICAL.MDR_REQUIREMENTS")

if __name__ == "__main__":
    main()
