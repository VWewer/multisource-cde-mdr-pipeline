"""
load_to_snowflake.py
multisource-cde-mdr-pipeline | Day 2 — Snowflake load

Creates (or replaces) three tables in Snowflake and loads CSV data into them:
  - RAW.DOCUMENTS
  - STAGED.EVENTS
  - ANALYTICAL.MDR_REQUIREMENTS

Prerequisites:
  - .env file in project root with Snowflake credentials (see .env.example)
  - CSVs generated by the three generate_*.py scripts in data_generation/

Run from project root:
  python data_generation/load_to_snowflake.py

Or from inside data_generation/:
  python load_to_snowflake.py
"""

import csv
import os
from pathlib import Path
from dotenv import load_dotenv
import snowflake.connector

# ── Resolve paths ──────────────────────────────────────────────────────────────
SCRIPT_DIR   = Path(__file__).parent
PROJECT_ROOT = SCRIPT_DIR.parent

# .env lives in project root
load_dotenv(PROJECT_ROOT / ".env")

RAW_CSV     = SCRIPT_DIR / "raw_documents.csv"
STAGED_CSV  = SCRIPT_DIR / "staged_events.csv"
MDR_CSV     = SCRIPT_DIR / "mdr_requirements.csv"

# ── DDL — table definitions ────────────────────────────────────────────────────

